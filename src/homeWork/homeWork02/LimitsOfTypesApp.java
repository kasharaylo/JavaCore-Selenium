package homeWork.homeWork02;
public class LimitsOfTypesApp {
    public static void main (String[] arg) {

        boolean a = true;
        boolean b = false;
        System.out.println("Boolean value can be " + a + " or " + b);

        System.out.println("Min value for Byte type is " + Byte.MIN_VALUE);
        System.out.println("Max value for Byte type is " + Byte.MAX_VALUE);

        System.out.println("Min value for Short type is " + Short.MIN_VALUE);
        System.out.println("Max value for Short type is " + Short.MAX_VALUE);

        System.out.println("Min value for Char type is " + Character.MIN_VALUE);
        System.out.println("Max value for Char type is " + Character.MAX_VALUE);

        System.out.println("Min value for Int type is " + Integer.MIN_VALUE);
        System.out.println("Max value for Int type is " + Integer.MAX_VALUE);

        System.out.println("Min value for Long type is " + Long.MIN_VALUE);
        System.out.println("Max value for Long type is " + Long.MAX_VALUE);

        System.out.println("Min value for Float type is " + Float.MIN_VALUE);
        System.out.println("Max value for Float type is " + Float.MAX_VALUE);

        System.out.println("Min value for Double type is " + Double.MIN_VALUE);
        System.out.println("Max value for Double type is " + Double.MAX_VALUE);
    }
}
